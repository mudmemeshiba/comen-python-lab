n = int(input("n: "))
count = (n*2)+3

for i in range(n):
    print(" "*i + "="*count + " "*i)
    print(" "*i + "=" + "#"*(count-2) + "=" + " "*i)
    count -= 2

count = 1
for i in range(n//2):
    print(" "*(n+1) + "="*(count))
    print(" "*n + "=" + "#"*(count) + "=")
    n -= 1
    count += 2

